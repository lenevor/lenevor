namespace Syscodes\Components\Core\Console\Commands;

use Symfony\Component\Console\Attribute\AsCommand;
use Syscodes\Components\Console\Command;

/**
 * Allows clear compiled of commands.
 */
#[AsCommand(name: 'clear-compiled')]
class ClearCompiledCommand extends Command
{
    /**
     * The console command name.
     *
     * @var string
     */
    protected $name = 'clear-compiled';

    /**
     * The console command description.
     *
     * @var string
     */
    protected $description = 'Remove the compiled class file';

    /**
     * Execute the console command.
     *
     * @return void
     */
    public function handle()
    {
        if (is_file($servicesPath = $this->lenevor->getCachedServicesPath())) {
            @unlink($servicesPath);
        }

        $this->components->info('Compiled services files removed successfully.');
    }
}